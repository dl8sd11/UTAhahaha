lll gcd(lll a, lll b){
    if (b==0) return a;
    return gcd(b,a%b);
}

lll po(lll n){
    lll x = 2, y = 2, d = 1;
    while (d==1){
        x = g(x,n); y = g(g(y,n),n); 
        d = gcd(x>y?x-y:y-x,n);
    }
    if (d==n) return -1;
    return d;
}

